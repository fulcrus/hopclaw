// RenderLetter converts a stored glyph to a tinted RGBA image.
func RenderLetter(letter rune, hexColor string) *image.RGBA {
	img := image.NewRGBA(image.Rect(0, 0, 16, 16))
	upper := letter
	if upper >= 'a' && upper <= 'z' {
		upper = upper - 'a' + 'A'
	}
	if upper < 'A' || upper > 'Z' {
		return img
	}

	_, fill, err := normalizeHexColor(hexColor)
	if err != nil {
		_, fill, _ = normalizeHexColor(defaultColor)
	}

	bitmap := letterBitmaps[upper-'A']
	for y, row := range bitmap {
		for x := 0; x < 16; x++ {
			mask := uint16(1 << (15 - x))
			if row&mask == 0 {
				continue
			}
			img.SetRGBA(x, y, fill)
		}
	}
	return img
}
